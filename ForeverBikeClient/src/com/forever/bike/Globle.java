package com.forever.bike;

public final class Globle {
	String userId;
	String sessionId;
	
	Globle(){
		userId="";
		sessionId="";
	}
	public String getUserId() {
		return userId;
	}
	public String getSessionId() {
		return sessionId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public void setSessionId(String sessionId) {
		this.sessionId = sessionId;
	}
	
	
	
	
}
